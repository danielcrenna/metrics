namespace Metrics
{
    /// <summary>
    ///     Provides support for timing values
    ///     <see href="http://download.oracle.com/javase/6/docs/api/java/util/concurrent/TimeUnit.html" />
    /// </summary>
    public enum TimeUnit
    {
        Nanoseconds = 0,
        Microseconds = 1,
        Milliseconds = 2,
        Seconds = 3,
        Minutes = 4,
        Hours = 5,
        Days = 6
    }
}